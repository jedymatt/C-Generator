    public String allRecord() {
        StringBuilder sb = new StringBuilder();
        sb.append("Written Works").append(System.getProperty("line.separator"));
        sb.append(classRecord.studentsWrittenRecord()).append(System.getProperty("line.separator"));
        sb.append(System.getProperty("line.separator"));
        sb.append("Performance Tasks").append(System.getProperty("line.separator"));
        sb.append(classRecord.studentsPerformanceRecord()).append(System.getProperty("line.separator"));
        sb.append(System.getProperty("line.separator"));
        sb.append("Quarterly Assessment").append(System.getProperty("line.separator"));
        sb.append(classRecord.studentsQuarterlyRecord()).append(System.getProperty("line.separator"));
        return sb.toString();
    }

