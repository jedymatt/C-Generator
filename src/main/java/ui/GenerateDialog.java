    private void buttonSaveFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveFileActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == buttonSaveFile) {
            fChooser.setSelectedFile(new java.io.File("c-generator-output.txt"));
            int returnVal = fChooser.showSaveDialog(this);
            if (returnVal == javax.swing.JFileChooser.APPROVE_OPTION) {
                try {
                    java.io.FileWriter writer = new java.io.FileWriter(fChooser.getSelectedFile());
                    writer.write(allRecord());
                    writer.close();
                } catch (IOException ex) {
                    System.err.println(ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_buttonSaveFileActionPerformed

